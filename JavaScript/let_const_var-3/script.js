
//let keyword
//we can modify the value of a variable sometime
let firstname = "satya";
firstname = "Vanapalli Satya";

console.log("Name: " + firstname);

//const keyword
//if once assign a value. we can't modify the value. because it is a fixed value.
const age = 21;
// age = 31; ---> It throws an error.
console.log("Age: " + age);

//var keyword
//It is similar to let keyword
var birthYear = 2000;
birthYear = 2003;
console.log("DOB: " + birthYear);







